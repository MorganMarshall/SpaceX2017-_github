function function29() {

  xhr = new XMLHttpRequest();
  xhr.open("GET", "flights/flight29.html", true);
  xhr.send();
  xhr.onreadystatechange = function() {
    if (xhr.readyState == 4 && xhr.status == 200) {
      document.getElementById("main").innerHTML = xhr.responseText;

    }
  }
}
